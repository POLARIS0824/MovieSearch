public class Cocktail{
    private String name;
    private float baseStrength;
    private int baseVolume;
    private int nonAlcoholicBeveragesVolume;

    public Cocktail(String name, float baseStrength, int baseVolume, int nonAlcoholicBeveragesVolume) {
        this.name = name;
        this.baseStrength = baseStrength;
        this.baseVolume = baseVolume;
        this.nonAlcoholicBeveragesVolume = nonAlcoholicBeveragesVolume;
    }

}
